    /**
     * Sets the table for displaying all the available expenses
     */
    public  void ShowAllSalesDetails()
    {
        try{
            DefaultTableModel dm = (DefaultTableModel)VSPS_Sales_Table.getModel();
            dm.setRowCount(0);
            this.sales = new SalesManager().GetAllSalesAvailable();
            if(null != sales)
            {
                sales.stream().forEach((sale) -> 
                {
                    dm.addRow(new Object[]
                    {
                        sale.GetID(),
                        sale.GetStockID(),
                        sale.GetStockName(),
                        sale.GetQuantity(),
                        sale.GetAmount(),
                        sale.GetGST(),
                        sale.GetFinalAmount(),
                        sale.GetDate()
                     });
                });
            }
        } 
        catch (ClassNotFoundException | SQLException ex) 
        {
           JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

