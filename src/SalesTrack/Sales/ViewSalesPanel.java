    private void VSPS_search_TFKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_VSPS_search_TFKeyReleased
        // When search words are typed on the search textfield
        SalesManager salesManager = null; 
        ResultSet rs = null; 
        
        String searchedValue = VSPS_search_TF.getText();
        if( searchedValue.length() > 0 )
        {
            try 
            {
                salesManager = new SalesManager();
                rs = salesManager.GetSalesDetailsOnSearching(searchedValue);
                if ( null == rs )
                {
                    
                }
                else
                {
                   VSPS_Sales_Table.setModel(DbUtils.resultSetToTableModel(rs));
                }
                
                // deallocation
                salesManager.closeConection();
                salesManager    = null;
                rs         = null;
                
            } 
            catch (ClassNotFoundException | SQLException ex) 
            {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_VSPS_search_TFKeyReleased


