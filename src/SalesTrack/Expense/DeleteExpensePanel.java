    private void DeleteExpense_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteExpense_ButtonActionPerformed
        // Deleting an income
        ExpenseManager expenseManager = null;
        ResultSet rs = null;
        int confirm = 0;
        boolean isDeleted = false;
        if(DEP_Expense_Table.getSelectedRow() >= 0)
        {
            // Showing a confirmation dialog
            confirm = JOptionPane.showConfirmDialog(null, "Are you sure, that you want to delete the selected expense details?");
            
            if (0 == confirm)
            {
                try
                {
                    expenseManager = new ExpenseManager();
                    isDeleted = expenseManager.DeleteExpenseDetails(String.valueOf(DEP_Expense_Table.getValueAt(DEP_Expense_Table.getSelectedRow(), 0)));
                    if(isDeleted)
                    {
                        JOptionPane.showMessageDialog(null, "Record has been deleted...");
                        this.ShowAllExpenseDetails();
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null, "Record could not be deleted...");
                    }   
                }
                catch (ClassNotFoundException | SQLException ex)
                {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, " Please select a row to continue...");
        }
    }//GEN-LAST:event_DeleteExpense_ButtonActionPerformed

    private void DEP_search_TFKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_DEP_search_TFKeyReleased
        // When search words are typed on the search textfield
        ExpenseManager expenseManager = null; 
        ResultSet rs = null; 
        
        String searchedValue = DEP_search_TF.getText();
        if( searchedValue.length() > 0 )
        {
            try 
            {
                expenseManager = new ExpenseManager();
                rs = expenseManager.GetExpenseDetailsOnSearching(searchedValue);
                if ( null == rs )
                {
                    
                }
                else
                {
                   DEP_Expense_Table.setModel(DbUtils.resultSetToTableModel(rs));
                }
                
                // deallocation
                expenseManager.closeConection();
                expenseManager    = null;
                rs         = null;
                
            } 
            catch (ClassNotFoundException | SQLException ex) 
            {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_DEP_search_TFKeyReleased


