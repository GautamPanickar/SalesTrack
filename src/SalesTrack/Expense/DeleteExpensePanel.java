    public  void ShowAllExpenseDetails()
    {
        try{
            DefaultTableModel dm = (DefaultTableModel)DEP_Expense_Table.getModel();
            dm.setRowCount(0);
            this.expenses = new ExpenseManager().GetAllExpensesAvailable();
            if(null != expenses)
            {
                expenses.stream().forEach((stock) -> 
                {
                    dm.addRow(new Object[]
                    {
                        stock.GetID(),
                        stock.GetName(),
                        stock.GetAmount(),
                        stock.GetDate()
                     });
                });
            }
        } 
        catch (ClassNotFoundException | SQLException ex) 
        {
           JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

