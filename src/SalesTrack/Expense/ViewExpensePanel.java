    private void VEP_search_TFKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_VEP_search_TFKeyReleased
        // When search words are typed on the search textfield
        ExpenseManager expenseManager = null; 
        ResultSet rs = null; 
        
        String searchedValue = VEP_search_TF.getText();
        if( searchedValue.length() > 0 )
        {
            try 
            {
                expenseManager = new ExpenseManager();
                rs = expenseManager.GetExpenseDetailsOnSearching(searchedValue);
                if ( null == rs )
                {
                    
                }
                else
                {
                   VEP_Expense_Table.setModel(DbUtils.resultSetToTableModel(rs));
                }
                
                // deallocation
                expenseManager.closeConection();
                expenseManager    = null;
                rs         = null;
                
            } 
            catch (ClassNotFoundException | SQLException ex) 
            {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_VEP_search_TFKeyReleased


