    /** 
     * Set the visibility of the panel in the card layout.
     *   Logic:
     *   First set visibility of all panel to false
     *  Then make the visibility of selected panel to true
     */
    private void SetPanelVisibility( JPanel panel ){
        
        MiddlePanel.removeAll();
        MiddlePanel.repaint();
        MiddlePanel.revalidate();
        
        MiddlePanel.add(panel);
        MiddlePanel.repaint();
        MiddlePanel.revalidate();
    }
    
    /**
     * Initializes the action panel with corresponding option
     * @param option 
     */
    private void ActionPanelInitialization(Functionality option)
    {
        // Initializes the constructor of actionPanel and passes the current object to actionPanel
        ActionPanel actionPanel = new ActionPanel(option);
        this.SetPanelVisibility(actionPanel);
    }
    
    private void SummaryActionPanelInitialization(Functionality option)
    {
        SummaryActionPanel actionPanel = new SummaryActionPanel(option);
        this.SetPanelVisibility(actionPanel);
    }

