    private void VCP_search_TFKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_VCP_search_TFKeyReleased
        // When search words are typed on the search textfield
        CreditManager creditManager = null; 
        ResultSet rs = null; 
        
        String searchedValue = VCP_search_TF.getText();
        if( searchedValue.length() > 0 )
        {
            try 
            {
                creditManager = new CreditManager();
                rs = creditManager.GetCreditDetailsOnSearching(searchedValue);
                if ( null == rs )
                {
                    
                }
                else
                {
                   VCP_Credit_Table.setModel(DbUtils.resultSetToTableModel(rs));
                }
                
                // deallocation
                creditManager.closeConection();
                creditManager    = null;
                rs         = null;
                
            } 
            catch (ClassNotFoundException | SQLException ex) 
            {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_VCP_search_TFKeyReleased


