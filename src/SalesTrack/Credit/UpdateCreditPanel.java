    private boolean IsAmountUpdateNeeded(Double amount)
    {
        if (AMOUNT_BEFORE_UPDATE == amount)
        {
            return false;
        }
        else
        {
            Double stepValue = amount - AMOUNT_BEFORE_UPDATE;
            COMPANY_AMOUNT_TO_BE_UPDATED = COMPANY_AMOUNT - stepValue;
            return true;
        }
    }
    
