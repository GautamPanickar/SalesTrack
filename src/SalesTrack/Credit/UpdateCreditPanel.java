    private void UCP_UpdateCredit_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UCP_UpdateCredit_ButtonActionPerformed
        boolean isCreditUpdated = false;
        boolean isCompanyMapped = false;
        int creditID = Integer.parseInt(UCP_CreditID_TF.getText());
        int stockID = Integer.parseInt(UCP_StockID_TF.getText());
        String name = UCP_Name_TF.getText();
        Double amount = Double.parseDouble(UCP_Amount_TF.getText());
        String dateText = ((JTextField)UCP_DateChooser.getDateEditor().getUiComponent()).getText();
        java.sql.Date date =  new java.sql.Date(UCP_DateChooser.getDate().getTime());
        String status = UCP_Status_CB.getSelectedItem().toString();
        
        if(UCP_CreditID_TF.getText().isEmpty() || UCP_StockID_TF.getText().isEmpty() || UCP_Name_TF.getText().isEmpty() || dateText.isEmpty()
                || UCP_Amount_TF.getText().isEmpty())
        {
               JOptionPane.showMessageDialog(null, "Cannot update with empty fields!");
        }
        else
        {
            try                    
            {
                StockManager stockManager = new StockManager();
                
                // Initializing a new expense
                Credit newCredit = new Credit(creditID, stockID, name, amount, date, status);
                CreditManager creditManager = new CreditManager();
                isCreditUpdated = creditManager.UpdateCredit(newCredit)? true: false;
                
                if (this.IsAmountUpdateNeeded(amount))
                {
                    
                    isCompanyMapped= stockManager.UpdateStockToCompanyMapAmount(COMPANY_ID, COMPANY_AMOUNT_TO_BE_UPDATED);
                        
                }

                if(isCreditUpdated)
                {
                    if(isCompanyMapped)
                    {
                        JOptionPane.showMessageDialog(null, "Credit and comapny details have been updated!");
                        this.SetFieldsEmpty();
                    }
                    else                        
                    {
                        JOptionPane.showMessageDialog(null, "Credit has been updated!");
                        this.SetFieldsEmpty();
                    }
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Error! while updating the credit. Pease check again.");
                }
            }
            catch (Exception ex)
            {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }          
                 
        }
    }//GEN-LAST:event_UCP_UpdateCredit_ButtonActionPerformed

