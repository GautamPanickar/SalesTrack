    /**
     * Sets the table for displaying all the available incomes
     */
    public  void ShowAllCreditDetails()
    {
        try{
            DefaultTableModel dm = (DefaultTableModel)DCP_Credit_Table.getModel();
            dm.setRowCount(0);
            this.credits = new CreditManager().GetAllCreditsAvailable();
            if(null != credits)
            {
                credits.stream().forEach((stock) -> 
                {
                    dm.addRow(new Object[]
                    {
                        stock.GetID(),
                        stock.GetCompanyID(),
                        stock.GetCompanyName(),
                        stock.GetAmount(),
                        stock.GetDate(),
                        stock.GetStatus()
                     });
                });
            }
        } 
        catch (ClassNotFoundException | SQLException ex) 
        {
           JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

