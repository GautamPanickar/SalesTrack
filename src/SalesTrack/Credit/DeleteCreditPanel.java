    private void DeleteCredit_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteCredit_ButtonActionPerformed
        // Deleting a credit
        CreditManager creditManager = null;
        ResultSet rs = null;
        int confirm = 0;
        boolean isDeleted = false, isMapped = false;
        if(DCP_Credit_Table.getSelectedRow() >= 0)
        {
            // Showing a confirmation dialog            
            if (0 == confirm)
            {
                try
                {
                    confirm = JOptionPane.showConfirmDialog(null, "Are you sure, that you want to delete the selected credit details?");  
                    
                    // STATIC DATA POPULATION
                    COMPANY_ID = Integer.parseInt(String.valueOf(DCP_Credit_Table.getValueAt(DCP_Credit_Table.getSelectedRow(), 1)));
                    COMPANY_NAME = String.valueOf(DCP_Credit_Table.getValueAt(DCP_Credit_Table.getSelectedRow(), 2));
                    StockManager manager = new StockManager();
                    COMPANY_AMOUNT = manager.GetCompanyAmountForStock(COMPANY_ID);                    
                    COMPANY_AMOUNT_TO_BE_UPDATED = COMPANY_AMOUNT + Double.valueOf(String.valueOf(DCP_Credit_Table.getValueAt(DCP_Credit_Table.getSelectedRow(), 3)));
                            
                    creditManager = new CreditManager();
                    isDeleted = creditManager.DeleteCreditDetails(String.valueOf(DCP_Credit_Table.getValueAt(DCP_Credit_Table.getSelectedRow(), 0)));
                    if(isDeleted)
                    {
                        isMapped = manager.UpdateStockToCompanyMapAmount(COMPANY_ID, COMPANY_AMOUNT_TO_BE_UPDATED);
                        if (isMapped)
                        {
                            JOptionPane.showMessageDialog(null, "Record has been deleted...");
                            this.ShowAllCreditDetails();
                        }
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null, "Record could not be deleted...");
                    }   
                }
                catch (ClassNotFoundException | SQLException ex)
                {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, " Please select a row to continue...");
        }
    }//GEN-LAST:event_DeleteCredit_ButtonActionPerformed

    private void DCP_search_TFKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_DCP_search_TFKeyReleased
        // When search words are typed on the search textfield
        CreditManager creditManager = null; 
        ResultSet rs = null; 
        
        String searchedValue = DCP_search_TF.getText();
        if( searchedValue.length() > 0 )
        {
            try 
            {
                creditManager = new CreditManager();
                rs = creditManager.GetCreditDetailsOnSearching(searchedValue);
                if ( null == rs )
                {
                    
                }
                else
                {
                   DCP_Credit_Table.setModel(DbUtils.resultSetToTableModel(rs));
                }
                
                // deallocation
                creditManager.closeConection();
                creditManager    = null;
                rs         = null;
                
            } 
            catch (ClassNotFoundException | SQLException ex) 
            {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_DCP_search_TFKeyReleased


